import pandas as pd
import plotly.express as px
import streamlit as st

# -----------------------------------------------------------
# 아주 간단한 앱: "정신질환 환자수"가 지역·질환마다 얼마나
# 퍼져 있는지(=흩어져 있는지) 딱 3가지 방법으로만 보는 앱이에요.
# -----------------------------------------------------------

st.set_page_config(page_title="정신질환 환자수 퍼짐 보기", page_icon="🧠")

st.title("🧠 정신질환 환자수, 지역마다 얼마나 다를까요?")
st.write(
    "건강보험심사평가원 자료를 가지고, 시군구·질환별 **총환자수**가 "
    "어떻게 퍼져 있는지 살펴보는 아주 간단한 앱이에요. "
    "값이 궁금하면 그래프 위에 마우스를 올려보세요 :)"
)

# -----------------------------------------------------------
# 1. 데이터 불러오기
#    - 이 CSV는 한글이 깨지지 않도록 cp949로 읽어야 해요.
# -----------------------------------------------------------
df = pd.read_csv(
    "data/시군구별_성별_주요_정신질환_통계_2024.csv",
    encoding="cp949",
)

# -----------------------------------------------------------
# 2. 가장 최신 연도만 남기기
#    - 지금은 2024년 하나뿐이지만, 나중에 연도가 늘어나도
#      항상 최신 연도만 보도록 안전장치를 걸어둬요.
# -----------------------------------------------------------
최신연도 = df["진료년도"].max()
df = df[df["진료년도"] == 최신연도]

# -----------------------------------------------------------
# 3. '총환자수' 열 만들기
#    - 이 데이터는 이미 시군구 단위이고(시도 합계행 없음),
#      한 행이 "시도+시군구+상병구분+성별" 조합 하나예요.
#    - 그래서 같은 지역·같은 질환끼리 '남' 환자수와 '여' 환자수를
#      더해주면, 지역·질환마다 하나의 총환자수 값이 나와요.
# -----------------------------------------------------------
총환자수_df = (
    df.groupby(["시도", "시군구", "상병구분"], as_index=False)["환자수"]
    .sum()
    .rename(columns={"환자수": "총환자수"})
)

st.caption(
    f"기준 연도: {최신연도}년 · 지역(시군구)×질환 조합 {len(총환자수_df):,}개의 "
    "'총환자수'를 살펴봅니다."
)

# -----------------------------------------------------------
# 4-1. describe() 표 보여주기
#    - 평균, 최솟값, 최댓값처럼 총환자수가 대략 어느 범위에
#      퍼져 있는지 숫자로 한눈에 볼 수 있어요.
# -----------------------------------------------------------
st.subheader("1️⃣ 총환자수 기초 통계 (describe)")
st.dataframe(총환자수_df["총환자수"].describe().to_frame("총환자수"))

# -----------------------------------------------------------
# 4-2. 히스토그램
#    - 총환자수가 작은 지역/질환이 많은지, 큰 지역/질환이 많은지
#      막대의 높이로 보여줘요.
# -----------------------------------------------------------
st.subheader("2️⃣ 총환자수 히스토그램")
fig_hist = px.histogram(
    총환자수_df,
    x="총환자수",
    nbins=40,
    labels={"총환자수": "총환자수 (명)"},
    color_discrete_sequence=["#FF7F50"],
)
fig_hist.update_layout(
    xaxis_title="총환자수 (명)",
    yaxis_title="지역·질환 조합 수",
    bargap=0.05,
)
st.plotly_chart(fig_hist, use_container_width=True)

# -----------------------------------------------------------
# 4-3. 상자그림(박스플롯)
#    - 가운데 값(중앙값)과 대부분의 값이 몰린 구간,
#      그리고 유난히 큰 값(이상치)을 한 번에 보여줘요.
# -----------------------------------------------------------
st.subheader("3️⃣ 총환자수 상자그림 (boxplot)")
fig_box = px.box(
    총환자수_df,
    y="총환자수",
    points="outliers",  # 유난히 튀는 지역·질환만 점으로 표시
    labels={"총환자수": "총환자수 (명)"},
    color_discrete_sequence=["#4C9AFF"],
)
fig_box.update_layout(yaxis_title="총환자수 (명)")
st.plotly_chart(fig_box, use_container_width=True)

st.info(
    "💡 상자그림에서 점으로 튀어나온 부분은 유난히 환자수가 많은 "
    "지역·질환 조합이에요. 어디인지 궁금하다면 다음 단계로 순위표나 "
    "지도를 추가해볼 수 있어요."
)
