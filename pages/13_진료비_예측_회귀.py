import glob

import pandas as pd
import plotly.graph_objects as go
import streamlit as st
from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score

# ================================================================
# 열세 번째 페이지: 오늘 배운 "연도→기온 예측기"와 완전히 같은
# 구조예요. 다만 연도 대신 "질환·나이대·지역"을 넣으면, 환자
# 1인당 평균 진료비를 예측해줘요.
#
# 5단계 그대로: ① 수집 → ② 전처리 → ③ 학습(model.fit) →
#              ④ 평가(R²) → ⑤ 예측(model.predict)
# ================================================================

st.set_page_config(page_title="진료비 예측기 (회귀)", page_icon="💵")

st.title("💵 진료비 예측기 (선형회귀)")
st.write(
    "질환·나이대·지역을 고르면, 환자 1인당 평균 진료비가 얼마쯤 될지 "
    "선형회귀로 예측해봐요. 오늘 배운 '연도→기온' 예측기와 순서는 "
    "완전히 똑같고, 입력만 슬라이더 대신 셀렉트박스로 바뀌었어요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]
연령_순서 = [
    "0~9세", "10~19세", "20~29세", "30~39세", "40~49세", "50~59세",
    "60~69세", "70~79세", "80~89세", "90~99세", "100세이상",
]

# ----------------------------------------------------------------
# ① 수집: 연령별 CSV 불러오기
#    - 파일 이름을 하드코딩하지 않고, data 폴더에서 '연령'이
#      들어간 CSV를 자동으로 찾아요.
# ----------------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    후보파일 = [f for f in glob.glob("data/*.csv") if "연령" in f]
    if not 후보파일:
        st.error("❌ data 폴더 안에서 '연령'이 들어간 CSV 파일을 찾지 못했어요.")
        st.stop()
    파일경로 = 후보파일[0]
    try:
        df = pd.read_csv(파일경로, encoding="cp949")
    except UnicodeDecodeError:
        df = pd.read_csv(파일경로, encoding="utf-8")
    최신연도 = df["진료년도"].max()
    df = df[df["진료년도"] == 최신연도]
    df = df.rename(columns={"상별구분": "상병구분"})
    return df


원본 = 데이터_불러오기()

# ----------------------------------------------------------------
# ② 전처리
#    - 성별은 합치고, 시도x연령대x질환 단위로 묶어요.
#    - 환자수가 0(마스킹)인 조합은 1인당 진료비를 계산할 수 없으니
#      학습에서 빼줘요.
# ----------------------------------------------------------------
집계 = 원본.groupby(["시도", "연령구분", "상병구분"], as_index=False).agg(
    환자수=("환자수", "sum"), 요양급여비용=("요양급여비용", "sum")
)
안전분모 = 집계["환자수"].where(집계["환자수"] > 0)
집계["1인당진료비"] = 집계["요양급여비용"] / 안전분모
학습자료 = 집계.dropna(subset=["1인당진료비"]).reset_index(drop=True)

st.caption(f"전처리 후 학습에 사용하는 데이터: {len(학습자료):,}개 (시도x연령대x질환 조합)")

# ----------------------------------------------------------------
# ③ 학습: 선형회귀
#    - 시도·연령대·질환은 숫자가 아니라 글자(카테고리)라서, 먼저
#      숫자로 바꿔주는 원-핫 인코딩을 거쳐요.
# ----------------------------------------------------------------
X = pd.get_dummies(학습자료[["시도", "연령구분", "상병구분"]])
y = 학습자료["1인당진료비"]


@st.cache_resource
def 모델_학습(X: pd.DataFrame, y: pd.Series):
    모델 = LinearRegression()
    모델.fit(X, y)
    return 모델


모델 = 모델_학습(X, y)
학습예측값 = 모델.predict(X)
r2 = r2_score(y, 학습예측값)

# ----------------------------------------------------------------
# ④ 평가: R² 카드
# ----------------------------------------------------------------
st.subheader("📊 모델 성능")
st.metric("R² (결정계수)", f"{r2:.3f}")
st.caption(
    "R²는 우리 모델이 실제 진료비 차이를 얼마나 잘 설명하는지 나타내는 "
    "값이에요 (1에 가까울수록 잘 맞아요). 참고로 여기서는 시험용 "
    "데이터를 따로 떼어놓지 않고, 학습에 쓴 데이터로 그대로 평가한 "
    "값이라 실제 성능보다 조금 후하게 나올 수 있어요."
)

# 실제값 vs 예측값 산점도 (대각선에 가까울수록 잘 맞는 모델)
fig = go.Figure()
fig.add_trace(
    go.Scatter(
        x=y, y=학습예측값, mode="markers", name="시도x연령대x질환 조합",
        marker=dict(color="royalblue"),
        text=학습자료["시도"] + " · " + 학습자료["연령구분"] + " · " + 학습자료["상병구분"],
        hovertemplate="%{text}<br>실제: %{x:,.0f}원<br>예측: %{y:,.0f}원<extra></extra>",
    )
)
최댓값 = max(y.max(), 학습예측값.max())
fig.add_trace(
    go.Scatter(
        x=[0, 최댓값], y=[0, 최댓값], mode="lines",
        line=dict(color="orange", dash="dash"), name="실제=예측 기준선",
    )
)
fig.update_layout(
    xaxis_title="실제 1인당 진료비 (원)",
    yaxis_title="모델 예측 1인당 진료비 (원)",
    height=460,
)
st.plotly_chart(fig, use_container_width=True)
st.caption("💡 점이 주황 점선(대각선)에 가까울수록 모델이 그 조합을 잘 맞혔다는 뜻이에요.")

# ----------------------------------------------------------------
# ⑤ 예측: 셀렉트박스로 조건 고르기
# ----------------------------------------------------------------
st.divider()
st.subheader("🔮 조건을 골라 진료비를 예측해보세요")

col1, col2, col3 = st.columns(3)
with col1:
    선택_질환 = st.selectbox("질환", 질환목록, index=질환목록.index("우울증"))
with col2:
    선택_연령 = st.selectbox("연령대", 연령_순서, index=연령_순서.index("30~39세"))
with col3:
    선택_시도 = st.selectbox("지역(시도)", sorted(학습자료["시도"].unique().tolist()))

if st.button("💵 예측하기", type="primary"):
    입력 = pd.DataFrame(
        [{"시도": 선택_시도, "연령구분": 선택_연령, "상병구분": 선택_질환}]
    )
    입력_인코딩 = pd.get_dummies(입력).reindex(columns=X.columns, fill_value=0)
    예측값 = 모델.predict(입력_인코딩)[0]

    st.metric(
        f"{선택_시도} · {선택_연령} · {선택_질환} 예측 1인당 진료비",
        f"{예측값:,.0f} 원",
    )

    # 이 조합이 실제 학습 데이터에 있었는지 확인해서, 없으면
    # '외삽(추정)'이라는 걸 알려줘요 (연도 범위 밖 예측 경고와 같은 역할).
    있음 = (
        (학습자료["시도"] == 선택_시도)
        & (학습자료["연령구분"] == 선택_연령)
        & (학습자료["상병구분"] == 선택_질환)
    ).any()
    if not 있음:
        st.warning(
            "⚠️ 참고용, 조심! 이 조합은 마스킹(10명 미만)되어 실제 학습 "
            "데이터에 없었어요. 다른 조합들로부터 계산을 더해서 나온 "
            "추정값이라 오차가 클 수 있어요."
        )
