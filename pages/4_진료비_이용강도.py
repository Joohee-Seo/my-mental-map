import pandas as pd
import plotly.express as px
import plotly.graph_objects as go
import streamlit as st

# -----------------------------------------------------------
# 네 번째 페이지: 질환마다 "진료비는 비싼데 병원은 뜸하게
# 가는지", "진료비는 싼데 자주 가는지" 같은 패턴을
# 버블차트(bubble chart)로 살펴봐요.
# -----------------------------------------------------------

st.set_page_config(page_title="진료비·이용강도 비교", page_icon="💰")

st.title("💰 진료비는 얼마나, 병원은 얼마나 자주?")
st.write(
    "환자 한 명당 평균 진료비와, 환자 한 명당 평균 내원일수를 질환·지역별로 "
    "비교해봐요. 버블(원)이 클수록 그 지역·질환의 전체 환자수가 많다는 뜻이에요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# -----------------------------------------------------------
# 지역x질환 단위로 합치고, 1인당 진료비 / 1인당 내원일수 계산
#    - 환자수가 0(10명 미만 마스킹)인 경우는 나누기를 하면
#      의미가 없으니 계산에서 빼줘요(NaN 처리).
# -----------------------------------------------------------
지역질환별 = df.groupby(["시도", "시군구", "상병구분"], as_index=False).agg(
    환자수=("환자수", "sum"),
    입내원일수=("입내원일수", "sum"),
    요양급여비용=("요양급여비용", "sum"),
)

환자수_0아닌것만 = 지역질환별["환자수"].where(지역질환별["환자수"] > 0)
지역질환별["1인당진료비"] = 지역질환별["요양급여비용"] / 환자수_0아닌것만
지역질환별["환자당내원일수"] = 지역질환별["입내원일수"] / 환자수_0아닌것만

# 계산이 안 된(마스킹된) 행은 버블차트에서 제외
지역질환별 = 지역질환별.dropna(subset=["1인당진료비", "환자당내원일수"])

# -----------------------------------------------------------
# 백분위(전국에서 몇 % 위치인지) 미리 계산해두기
#    - 같은 질환끼리만 비교해야 공정해서, 질환별로 따로 계산해요.
# -----------------------------------------------------------
지역질환별["진료비_백분위"] = (
    지역질환별.groupby("상병구분")["1인당진료비"].rank(pct=True) * 100
).round(0)
지역질환별["내원일수_백분위"] = (
    지역질환별.groupby("상병구분")["환자당내원일수"].rank(pct=True) * 100
).round(0)

st.write(
    "**이 그래프가 필요한 이유**: 환자수나 총비용만 보면 규모가 큰 지역이 늘 "
    "눈에 띄지만, '1인당' 기준으로 바꿔보면 어떤 질환이 실제로 진료비 부담이 "
    "크고 병원을 자주 다녀야 하는지가 드러나요."
)

로그스케일 = st.checkbox("가로·세로축을 로그 스케일로 보기 (값 차이가 클 때 유용)", value=True)

# -----------------------------------------------------------
# 궁금한 지역 하나 골라서 강조해보기
# -----------------------------------------------------------
st.subheader("🔎 궁금한 지역, 콕 짚어서 보기")
st.write(
    "점이 너무 몰려 있어서 내가 궁금한 지역이 어디 있는지 찾기 어려울 때, "
    "아래에서 지역을 고르면 그래프에 동그란 테두리로 표시해줘요."
)

col1, col2 = st.columns(2)
with col1:
    시도_옵션 = ["(선택 안 함)"] + sorted(지역질환별["시도"].unique().tolist())
    선택_시도 = st.selectbox("시도", 시도_옵션)

if 선택_시도 != "(선택 안 함)":
    시군구_옵션 = sorted(
        지역질환별[지역질환별["시도"] == 선택_시도]["시군구"].unique().tolist()
    )
else:
    시군구_옵션 = []

with col2:
    선택_시군구 = st.selectbox("시군구", 시군구_옵션) if 시군구_옵션 else None

강조_대상 = None
if 선택_시도 != "(선택 안 함)" and 선택_시군구:
    강조_대상 = 지역질환별[
        (지역질환별["시도"] == 선택_시도) & (지역질환별["시군구"] == 선택_시군구)
    ]

# -----------------------------------------------------------
# 그래프 그리기
# -----------------------------------------------------------
fig = px.scatter(
    지역질환별,
    x="1인당진료비",
    y="환자당내원일수",
    size="환자수",
    color="상병구분",
    hover_name="시군구",
    hover_data={"시도": True, "환자수": ":,.0f"},
    category_orders={"상병구분": 질환목록},
    log_x=로그스케일,
    log_y=로그스케일,
    size_max=40,
    labels={
        "1인당진료비": "환자 1인당 평균 진료비 (원)",
        "환자당내원일수": "환자 1인당 평균 내원일수 (일)",
        "상병구분": "질환",
    },
)

# 선택한 지역이 있으면, 그 지역의 점들 위에 검은 테두리 링을 덧그려요.
if 강조_대상 is not None and len(강조_대상) > 0:
    fig.add_trace(
        go.Scatter(
            x=강조_대상["1인당진료비"],
            y=강조_대상["환자당내원일수"],
            mode="markers+text",
            marker=dict(
                size=26,
                color="rgba(0,0,0,0)",  # 안은 비워두고
                line=dict(color="#39FF14", width=3),  # 테두리만 두껍게(형광 라임색)
            ),
            text=강조_대상["상병구분"],
            textposition="top center",
            name=f"⭐ {선택_시군구}",
            hoverinfo="skip",
        )
    )

fig.update_layout(
    xaxis_title="환자 1인당 평균 진료비 (원)",
    yaxis_title="환자 1인당 평균 내원일수 (일)",
    legend_title_text="질환",
)
st.plotly_chart(fig, use_container_width=True)

st.info(
    "💡 오른쪽 위에 있는 버블일수록 진료비도 비싸고 병원도 자주 가야 하는 "
    "질환·지역이고, 왼쪽 아래는 그 반대예요."
)

# -----------------------------------------------------------
# 선택한 지역의 백분위(전국에서 몇 % 위치인지)를 표로 보여주기
# -----------------------------------------------------------
if 강조_대상 is not None:
    if len(강조_대상) > 0:
        st.subheader(f"📍 {선택_시도} {선택_시군구}, 질환별 전국 위치")
        st.write(
            "백분위가 100에 가까울수록 같은 질환의 다른 지역들보다 "
            "**값이 높은 쪽(상위권)**, 0에 가까울수록 **낮은 쪽(하위권)**이에요."
        )
        표시용 = 강조_대상[
            ["상병구분", "1인당진료비", "진료비_백분위", "환자당내원일수", "내원일수_백분위"]
        ].rename(
            columns={
                "상병구분": "질환",
                "1인당진료비": "1인당 진료비(원)",
                "진료비_백분위": "진료비 백분위(%)",
                "환자당내원일수": "1인당 내원일수(일)",
                "내원일수_백분위": "내원일수 백분위(%)",
            }
        )
        st.dataframe(
            표시용.style.format(
                {"1인당 진료비(원)": "{:,.0f}", "1인당 내원일수(일)": "{:.1f}"}
            ),
            use_container_width=True,
            hide_index=True,
        )
    else:
        st.caption("이 지역은 마스킹(10명 미만)된 질환이 많아 표시할 데이터가 없어요.")
