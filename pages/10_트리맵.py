import pandas as pd
import plotly.express as px
import streamlit as st

# -----------------------------------------------------------
# 열 번째 페이지: 시도 → 시군구 → 질환 순서로 네모 상자가
# 겹겹이 쌓인 트리맵이에요. 상자 크기가 클수록 환자수가 많다는
# 뜻이고, 상자를 클릭하면 그 안쪽을 확대해서 볼 수 있어요.
# -----------------------------------------------------------

st.set_page_config(page_title="전체 한눈에 보기 (트리맵)", page_icon="🌳")

st.title("🌳 전체를 한눈에: 숲과 나무 같이 보기")
st.write(
    "지금까지는 지역 하나씩 또는 질환 하나씩 봤다면, 이번엔 **전국 → 시도 → "
    "시군구 → 질환**이 어떻게 다 같이 맞물려 있는지 상자 하나로 살펴봐요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# 부천시/군위군처럼 행정구역 변경으로 남은, 전부 0인 옛 표기 지역은 제외
지역별_합계 = df.groupby(["시도", "시군구"])["환자수"].transform("sum")
df = df[지역별_합계 > 0]

st.write(
    "**이 그래프가 필요한 이유**: 표나 막대그래프는 한 번에 한두 가지 기준만 "
    "보여주지만, 트리맵은 지역과 질환을 겹겹이 쌓아서 전체 크기 비교와 "
    "세부 비교를 동시에 보여줘요. 상자를 클릭하면 그 안으로 확대돼요."
)

집계 = df.groupby(["시도", "시군구", "상병구분"], as_index=False)["환자수"].sum()
# 트리맵은 값이 0이면 상자를 그릴 수 없어서, 아주 작은 지역·질환 조합은
# 자연스럽게 화면에서 작은 상자로만 표시돼요 (제외할 필요 없음).
집계 = 집계[집계["환자수"] > 0]

fig = px.treemap(
    집계,
    path=[px.Constant("전국"), "시도", "시군구", "상병구분"],
    values="환자수",
    color="상병구분",
    maxdepth=3,  # 처음엔 전국→시도→시군구까지만 보여주고, 클릭하면 질환까지 확대
    labels={"상병구분": "질환"},
)
fig.update_traces(hovertemplate="%{label}<br>환자수: %{value:,.0f}명<extra></extra>")
fig.update_layout(height=650, margin=dict(l=0, r=0, t=10, b=0))
st.plotly_chart(fig, use_container_width=True)

st.info(
    "💡 상자를 클릭하면 그 안으로 확대돼서 하위 항목(시군구, 질환)이 보이고, "
    "가운데 위쪽 경로를 클릭하면 다시 밖으로 나올 수 있어요."
)
