import pandas as pd
import streamlit as st

# -----------------------------------------------------------
# 여덟 번째 페이지: 그래프보다 "정확히 몇 등이야?"가 궁금할 때
# 보는, 상위 20개 · 하위 20개 지역 순위표예요.
# -----------------------------------------------------------

st.set_page_config(page_title="지역 순위표", page_icon="🏆")

st.title("🏆 어느 지역이 몇 등일까요?")
st.write(
    "그래프로 대략적인 분포를 봤다면, 이번엔 정확한 순위를 표로 확인해봐요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]
지표_라벨 = {
    "환자수": "환자수 (명)",
    "입내원일수": "입내원일수 (일)",
    "요양급여비용": "요양급여비용 (원)",
    "1인당진료비": "환자 1인당 평균 진료비 (원)",
    "환자당내원일수": "환자 1인당 평균 내원일수 (일)",
}


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# 부천시/군위군처럼 행정구역 변경으로 남은, 전부 0인 옛 표기 지역은 제외
지역별_합계 = df.groupby(["시도", "시군구"])["환자수"].transform("sum")
df = df[지역별_합계 > 0]

col1, col2 = st.columns(2)
with col1:
    질환 = st.selectbox("🔍 질환 선택", 질환목록, index=질환목록.index("우울증"))
with col2:
    지표 = st.selectbox(
        "📊 기준 지표", list(지표_라벨.keys()), format_func=lambda x: 지표_라벨[x]
    )

st.write(
    f"**이 표가 필요한 이유**: '{질환}'을 **{지표_라벨[지표]}** 기준으로 줄 세워서, "
    "상위 20개·하위 20개 지역이 정확히 어디인지 이름으로 바로 확인할 수 있어요."
)

# -----------------------------------------------------------
# 지역x질환 단위로 합치고, 필요하면 1인당 지표도 계산
# -----------------------------------------------------------
집계 = df.groupby(["시도", "시군구", "상병구분"], as_index=False).agg(
    환자수=("환자수", "sum"),
    입내원일수=("입내원일수", "sum"),
    요양급여비용=("요양급여비용", "sum"),
)
환자수_0아닌것만 = 집계["환자수"].where(집계["환자수"] > 0)
집계["1인당진료비"] = (집계["요양급여비용"] / 환자수_0아닌것만).round(0)
집계["환자당내원일수"] = (집계["입내원일수"] / 환자수_0아닌것만).round(1)

대상 = 집계[집계["상병구분"] == 질환].dropna(subset=[지표]).sort_values(지표, ascending=False)

표시컬럼 = ["시도", "시군구", 지표]

st.subheader(f"🥇 {질환} · {지표_라벨[지표]} 상위 20개 지역")
st.dataframe(
    대상.head(20)[표시컬럼].reset_index(drop=True).style.format({지표: "{:,.0f}"}),
    use_container_width=True,
)

st.subheader(f"🥉 {질환} · {지표_라벨[지표]} 하위 20개 지역")
st.dataframe(
    대상.tail(20)[표시컬럼].sort_values(지표).reset_index(drop=True).style.format(
        {지표: "{:,.0f}"}
    ),
    use_container_width=True,
)

if 지표 == "환자수":
    st.caption(
        "⚠️ 환자수는 10명 미만이면 0으로 표기(마스킹)되어 있어서, 하위 20개 "
        "지역 중 상당수가 '실제로 적음'이 아니라 '마스킹'일 수 있어요."
    )
