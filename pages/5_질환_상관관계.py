import pandas as pd
import plotly.express as px
import streamlit as st

# -----------------------------------------------------------
# 다섯 번째 페이지: "우울증이 많은 지역은 불안장애도 많을까?"
# 처럼, 두 질환이 지역별로 같이 늘고 줄어드는 경향(상관관계)이
# 있는지 히트맵으로 살펴봐요.
# -----------------------------------------------------------

st.set_page_config(page_title="질환 간 상관관계", page_icon="🔗")

st.title("🔗 어떤 질환들이 지역별로 함께 나타날까요?")
st.write(
    "어떤 질환은 같은 지역에서 함께 많아지는 경향이 있을 수 있어요. "
    "6개 질환끼리 지역별로 얼마나 닮은 패턴을 보이는지 상관관계로 살펴봐요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# -----------------------------------------------------------
# 원자료 정리: 행정구역이 바뀌면서(예: 부천시→3개 구 분리,
# 군위군→대구 편입) 옛날식 이름이 "모든 값이 0"인 빈 줄로
# 같이 남아있는 경우가 있어요. 실제 값은 새 이름(부천소사구 등)
# 쪽에 들어있으니, 이 빈 줄만 조용히 걸러내요(상관계수 왜곡 방지).
# -----------------------------------------------------------
지역별_합계 = df.groupby(["시도", "시군구"])["환자수"].transform("sum")
df = df[지역별_합계 > 0]

st.write(
    "**이 그래프가 필요한 이유**: 질환이 6개나 되면 표로는 관계를 파악하기 "
    "어려워요. 상관계수(-1~1) 하나로 요약해서 색으로 보여주면, 어떤 질환끼리 "
    "지역별로 '같이 많고 같이 적은지'가 한눈에 들어와요."
)

# -----------------------------------------------------------
# 시군구 x 질환 형태로 펼치기 (한 행 = 하나의 시군구,
# 열 하나하나가 그 지역의 질환별 환자수)
# -----------------------------------------------------------
지역별_질환 = df.groupby(["시도", "시군구", "상병구분"], as_index=False)["환자수"].sum()
가로형 = 지역별_질환.pivot_table(
    index=["시도", "시군구"], columns="상병구분", values="환자수", fill_value=0
)
가로형 = 가로형[질환목록]  # 보기 좋게 질환 순서 고정

# -----------------------------------------------------------
# 상관계수 계산 (질환 x 질환, 253개 지역을 기준으로)
# -----------------------------------------------------------
상관행렬 = 가로형.corr().round(2)

fig = px.imshow(
    상관행렬,
    text_auto=True,
    color_continuous_scale="RdBu_r",
    zmin=-1,
    zmax=1,
    labels=dict(color="상관계수"),
)
fig.update_layout(height=520)
st.plotly_chart(fig, use_container_width=True)

# -----------------------------------------------------------
# 가장 상관관계가 강한 질환 쌍 하나를 자동으로 찾아서 알려주기
# -----------------------------------------------------------
쌍_목록 = []
for i in range(len(질환목록)):
    for j in range(i + 1, len(질환목록)):
        쌍_목록.append((질환목록[i], 질환목록[j], 상관행렬.iloc[i, j]))
가장_강한_쌍 = max(쌍_목록, key=lambda x: x[2])

st.info(
    f"💡 상관계수가 가장 높은 조합은 **{가장_강한_쌍[0]} ↔ {가장_강한_쌍[1]}**"
    f"(상관계수 {가장_강한_쌍[2]})이에요. 1에 가까울수록 두 질환이 지역별로 "
    "'같이 많고 같이 적은' 경향이 강하다는 뜻이고, 0에 가까우면 서로 별 상관이 "
    "없다는 뜻이에요."
)
