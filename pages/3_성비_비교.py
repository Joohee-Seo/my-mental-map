import pandas as pd
import plotly.express as px
import plotly.graph_objects as go
import streamlit as st

# -----------------------------------------------------------
# 세 번째 페이지: 같은 지역이라도 남자·여자 환자수가 얼마나
# 다른지, 질환마다 점 하나(=지역 하나)로 표현해서 살펴봐요.
# -----------------------------------------------------------

st.set_page_config(page_title="성비 비교", page_icon="⚖️")

st.title("⚖️ 지역마다 남녀 환자수, 얼마나 다를까요?")
st.write(
    "표에는 숫자만 잔뜩 있어서 잘 안 보이지만, 그래프로 그리면 "
    "'이 지역은 여자 환자가 유독 많네?' 같은 성비 편차가 한눈에 드러나요."
)
st.caption(
    "🧭 **7번(연령별 피라미드) 페이지와 다른 점**: 이 페이지는 **지역**을 "
    "기준으로 성비를 보고(어느 동네가 남녀 차이가 큰지), 7번 페이지는 "
    "**나이대**를 기준으로 성비를 봐요(몇 살 때 남녀 차이가 큰지). 같은 "
    "'성별 차이'를 지역 축으로 볼지, 나이 축으로 볼지의 차이예요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# -----------------------------------------------------------
# 질환 하나 고르기
#    - 질환마다 환자수 규모 차이가 커서, 한 번에 하나씩만
#      비교해야 그래프가 보기 편해요.
# -----------------------------------------------------------
질환 = st.selectbox("🔍 살펴볼 질환", 질환목록, index=질환목록.index("우울증"))

st.write(
    f"**이 그래프가 필요한 이유**: '{질환}' 환자수를 지역마다 남자·여자로 나눠서 "
    "점 하나씩 찍어보면(점 하나 = 지역 하나), 대각선(남녀 환자수가 같은 선)보다 "
    "위에 있는지 아래에 있는지로 그 지역의 성비 쏠림을 바로 알 수 있어요."
)

# -----------------------------------------------------------
# 성별을 옆으로 펼쳐서(남/여 각각 열로) 지역마다 한 행씩 만들기
# -----------------------------------------------------------
d = df[df["상병구분"] == 질환]
성별_가로형 = d.pivot_table(
    index=["시도", "시군구"], columns="성별", values="환자수", fill_value=0
).reset_index()

# 혹시 남/여 둘 중 하나라도 없는 지역이 있으면 0으로 채워줘요.
for col in ["남", "여"]:
    if col not in 성별_가로형.columns:
        성별_가로형[col] = 0

# -----------------------------------------------------------
# 궁금한 지역 하나 골라서 강조해보기 (4번 페이지와 같은 방식)
# -----------------------------------------------------------
st.subheader("🔎 궁금한 지역, 콕 짚어서 보기")
col1, col2 = st.columns(2)
with col1:
    시도_옵션 = ["(선택 안 함)"] + sorted(성별_가로형["시도"].unique().tolist())
    선택_시도 = st.selectbox("시도", 시도_옵션)

if 선택_시도 != "(선택 안 함)":
    시군구_옵션 = sorted(
        성별_가로형[성별_가로형["시도"] == 선택_시도]["시군구"].unique().tolist()
    )
else:
    시군구_옵션 = []

with col2:
    선택_시군구 = st.selectbox("시군구", 시군구_옵션) if 시군구_옵션 else None

강조_대상 = None
if 선택_시도 != "(선택 안 함)" and 선택_시군구:
    강조_대상 = 성별_가로형[
        (성별_가로형["시도"] == 선택_시도) & (성별_가로형["시군구"] == 선택_시군구)
    ]

최댓값 = max(성별_가로형["남"].max(), 성별_가로형["여"].max())

fig = px.scatter(
    성별_가로형,
    x="남",
    y="여",
    hover_name="시군구",
    hover_data={"시도": True},
    labels={"남": "남자 환자수 (명)", "여": "여자 환자수 (명)"},
    color_discrete_sequence=["#FF6B9D"],
)

# 대각선(남녀 환자수가 똑같은 기준선) 그려주기
fig.add_trace(
    go.Scatter(
        x=[0, 최댓값],
        y=[0, 최댓값],
        mode="lines",
        line=dict(color="gray", dash="dash"),
        name="남녀 동일선",
        hoverinfo="skip",
    )
)

# 선택한 지역이 있으면, 검은 테두리 링으로 덧그려요.
if 강조_대상 is not None and len(강조_대상) > 0:
    fig.add_trace(
        go.Scatter(
            x=강조_대상["남"],
            y=강조_대상["여"],
            mode="markers",
            marker=dict(size=22, color="rgba(0,0,0,0)", line=dict(color="#39FF14", width=3)),
            name=f"⭐ {선택_시군구}",
            hoverinfo="skip",
        )
    )

fig.update_layout(
    xaxis_title="남자 환자수 (명)",
    yaxis_title="여자 환자수 (명)",
)
st.plotly_chart(fig, use_container_width=True)

st.info(
    "💡 점선(대각선)보다 **위쪽**에 있으면 그 지역은 **여자 환자가 더 많고**, "
    "**아래쪽**에 있으면 **남자 환자가 더 많다**는 뜻이에요."
)

# 선택한 지역의 남녀 차이를 문장으로 요약해주기
if 강조_대상 is not None and len(강조_대상) > 0:
    남 = int(강조_대상["남"].iloc[0])
    여 = int(강조_대상["여"].iloc[0])
    if 남 + 여 > 0:
        차이_비율 = abs(여 - 남) / (남 + 여) * 100
        많은쪽 = "여자" if 여 > 남 else "남자" if 남 > 여 else None
        st.subheader(f"📍 {선택_시도} {선택_시군구} · {질환}")
        c1, c2 = st.columns(2)
        c1.metric("남자 환자수", f"{남:,}명")
        c2.metric("여자 환자수", f"{여:,}명")
        if 많은쪽:
            st.write(f"→ 이 지역은 **{많은쪽} 환자가 약 {차이_비율:.0f}%p 더 많아요**.")
        else:
            st.write("→ 이 지역은 남녀 환자수가 같아요.")
    else:
        st.caption("이 지역은 마스킹(10명 미만)되어 표시할 데이터가 없어요.")
