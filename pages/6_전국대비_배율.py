import pandas as pd
import plotly.express as px
import streamlit as st

# -----------------------------------------------------------
# 여섯 번째 페이지: 환자수 그대로 보면 인구가 많은 지역(서울,
# 경기)이 항상 1등이에요. 그 대신 "전국 평균보다 몇 배 많은지"로
# 바꿔보면, 인구 크기 편향을 어느 정도 줄이고 볼 수 있어요.
# -----------------------------------------------------------

st.set_page_config(page_title="전국 대비 배율", page_icon="📐")

st.title("📐 전국 평균보다 몇 배 더 많을까요?")
st.write(
    "인구가 많은 지역은 환자수도 늘 많게 보여요. 그래서 '환자수'가 아니라 "
    "'전국 시군구 평균 대비 몇 배인지'로 바꿔서, 진짜 두드러지는 지역을 찾아봐요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# -----------------------------------------------------------
# 원자료 정리: 행정구역이 바뀌면서(예: 부천시→3개 구 분리,
# 군위군→대구 편입) 옛날식 이름이 "모든 값이 0"인 빈 줄로
# 같이 남아있는 경우가 있어요. 실제 값은 새 이름(부천소사구 등)
# 쪽에 들어있으니, 이 빈 줄만 조용히 걸러내요.
# -----------------------------------------------------------
지역별_합계 = df.groupby(["시도", "시군구"])["환자수"].transform("sum")
df = df[지역별_합계 > 0]

질환 = st.selectbox("🔍 살펴볼 질환", 질환목록, index=질환목록.index("우울증"))

st.write(
    f"**이 그래프가 필요한 이유**: '{질환}' 환자수를 그대로 순위를 매기면 "
    "인구가 많은 대도시가 항상 상위권이에요. 대신 **(그 지역 환자수) ÷ (전국 "
    "시군구 평균 환자수)** 로 배율을 계산하면, 인구 규모와 상관없이 "
    "'평균보다 유난히 많은 지역'이 드러나요."
)
st.caption(
    "⚠️ 다만 이건 인구수로 나눈 진짜 유병률이 아니라, '전국 평균 환자수' 대비 "
    "배율이에요(약식 지표). 정확한 유병률을 보려면 지역별 인구 데이터가 "
    "추가로 필요해요."
)

# -----------------------------------------------------------
# 선택한 질환의 지역별 환자수 & 전국 평균 대비 배율 계산
# -----------------------------------------------------------
지역별 = df[df["상병구분"] == 질환].groupby(["시도", "시군구"], as_index=False)["환자수"].sum()
전국평균 = 지역별["환자수"].mean()
지역별["전국평균대비_배율"] = (지역별["환자수"] / 전국평균).round(2)
지역별 = 지역별.sort_values("전국평균대비_배율", ascending=False)

st.caption(f"전국 시군구 평균 {질환} 환자수: 약 {전국평균:,.0f}명 (이 값을 1배 기준으로 삼았어요)")

fig = px.bar(
    지역별,
    x="전국평균대비_배율",
    y="시군구",
    orientation="h",
    color="전국평균대비_배율",
    color_continuous_scale="RdYlBu_r",
    hover_data={"시도": True, "환자수": ":,.0f"},
    labels={"전국평균대비_배율": "전국 평균 대비 배율", "시군구": "시군구"},
)
fig.add_vline(x=1, line_dash="dash", line_color="gray")
fig.update_layout(
    height=max(600, len(지역별) * 8),
    yaxis=dict(categoryorder="total ascending"),
    xaxis_title="전국 평균 대비 배율 (1배 = 전국 평균과 같음)",
    coloraxis_showscale=False,
)
st.plotly_chart(fig, use_container_width=True)

st.info(
    "💡 점선(1배 기준선)보다 오른쪽에 있으면 전국 평균보다 환자수가 많은 지역, "
    "왼쪽에 있으면 평균보다 적은 지역이에요. 그래프가 길어서 마우스로 확대해서 "
    "보시면 지역 이름이 잘 보여요."
)
