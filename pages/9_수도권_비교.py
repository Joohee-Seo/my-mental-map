import pandas as pd
import plotly.express as px
import streamlit as st

# -----------------------------------------------------------
# 아홉 번째 페이지: 수도권(서울·경기·인천)과 비수도권을 나눠서,
# 분포 모양 자체가 어떻게 다른지 상자그림으로 비교해봐요.
# -----------------------------------------------------------

st.set_page_config(page_title="수도권 vs 비수도권", page_icon="🏙️")

st.title("🏙️ 수도권과 비수도권, 분포가 다를까요?")
st.write(
    "평균만 비교하면 놓치기 쉬운 '들쭉날쭉한 정도(퍼짐)'까지, 상자그림으로 "
    "수도권과 비수도권을 나란히 비교해봐요."
)

질환목록 = ["ADHD", "불면증", "우울증", "조울증", "불안장애", "조현병"]
수도권_시도 = ["서울", "경기", "인천"]


# -----------------------------------------------------------
# 데이터 불러오기 (main.py와 똑같은 방식)
# -----------------------------------------------------------
@st.cache_data
def 데이터_불러오기():
    df = pd.read_csv(
        "data/시군구별_성별_주요_정신질환_통계_2024.csv",
        encoding="cp949",
    )
    최신연도 = df["진료년도"].max()
    return df[df["진료년도"] == 최신연도]


df = 데이터_불러오기()

# 부천시/군위군처럼 행정구역 변경으로 남은, 전부 0인 옛 표기 지역은 제외
지역별_합계 = df.groupby(["시도", "시군구"])["환자수"].transform("sum")
df = df[지역별_합계 > 0]

질환 = st.selectbox("🔍 질환 선택", 질환목록, index=질환목록.index("우울증"))

st.write(
    f"**이 그래프가 필요한 이유**: '{질환}' 환자수가 수도권과 비수도권에서 "
    "평균만 다른 건지, 아니면 지역 간 편차(퍼짐) 자체가 다른 건지를 함께 "
    "볼 수 있어요."
)

# -----------------------------------------------------------
# 시군구 단위로 합치고, 수도권/비수도권 구분 열 만들기
# -----------------------------------------------------------
지역별 = df[df["상병구분"] == 질환].groupby(["시도", "시군구"], as_index=False)["환자수"].sum()
지역별["구분"] = 지역별["시도"].apply(lambda x: "수도권" if x in 수도권_시도 else "비수도권")

fig = px.box(
    지역별,
    x="구분",
    y="환자수",
    color="구분",
    points="all",
    hover_data={"시도": True, "시군구": True},
    category_orders={"구분": ["수도권", "비수도권"]},
    color_discrete_map={"수도권": "#4C9AFF", "비수도권": "#FF7F50"},
    labels={"환자수": "환자수 (명)", "구분": ""},
)
fig.update_layout(showlegend=False, yaxis_title="환자수 (명)")
st.plotly_chart(fig, use_container_width=True)

# 두 그룹의 중앙값을 계산해서 짧게 비교 문장 보여주기
중앙값 = 지역별.groupby("구분")["환자수"].median()
st.info(
    f"💡 시군구 하나당 '{질환}' 환자수의 중앙값은 수도권 "
    f"{중앙값.get('수도권', 0):,.0f}명, 비수도권 {중앙값.get('비수도권', 0):,.0f}명이에요."
)
st.caption("수도권 = 서울·경기·인천, 비수도권 = 나머지 모든 시도")
